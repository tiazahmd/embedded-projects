int main(void)
{
	GPIO_InitTypeDef GPIO_A, GPIO_D;

	HAL_Init();

	__HAL_RCC_GPIOA_CLK_ENABLE();
	__HAL_RCC_GPIOD_CLK_ENABLE();

	GPIO_A.Pin = GPIO_PIN_0;
	GPIO_A.Mode = GPIO_MODE_INPUT;
	GPIO_A.Pull = GPIO_PULLDOWN;
	GPIO_A.Speed = GPIO_SPEED_FAST;

	GPIO_D.Pin = GPIO_PIN_12 | GPIO_PIN_14;
	GPIO_D.Mode = GPIO_MODE_OUTPUT_PP;
	GPIO_D.Pull = GPIO_NOPULL;

	HAL_GPIO_Init(GPIOA, &GPIO_A);
	HAL_GPIO_Init(GPIOD, &GPIO_D);

	while (1) {
		SetReady();
		WaitForASLow();
	}
}

void SetReady()
{
	HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, SET);
}

void ClearReady()
{
	HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, RESET);
}

void WaitForASLow(void)
{
	while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0)) {
		ClearReady();
		HAL_Delay(10);
		WaitForASHigh();
	}
}

void WaitForASHigh(void)
{
	while(!(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0))) {
		HAL_Delay(250);
		SetVT();
		HAL_Delay(250);
		ClearVT();
		break;
	}
}

void SetVT(void)
{
	HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, SET);
}

void ClearVT(void)
{
	HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, RESET);
}


